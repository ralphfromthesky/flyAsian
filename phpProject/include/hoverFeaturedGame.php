<script>
    const slider = $('#slider');
    const slides = $('.hoverFeatureGame');
    const slideWidth = slides.first().width() + 100;

    let currentIndex = 0;

    slider.width(`${slideWidth * slides.length}px`);

    $('#nextBtn').on('click', function () {
        if (currentIndex < slides.length - 1) {
            currentIndex++;
        }
        updateSlider();
    });

    $('#prevBtn').on('click', function () {
        if (currentIndex > 0) {
            currentIndex--;
        }
        updateSlider();
    });

    function updateSlider() {
        const translateValue = -currentIndex * slideWidth;
        slider.css('transform', `translateX(${translateValue}px)`);
        $('#nextBtn').prop('disabled', currentIndex === slides.length - 18);
    }

</script>